    protected function resolveLossDate(Policy $policy, Generator $faker): Carbon
    {
        $effective = $policy->effective_date
            ? Carbon::parse($policy->effective_date)->startOfDay()
            : now()->subMonths(6)->startOfDay();

        $expiration = $policy->expiration_date
            ? Carbon::parse($policy->expiration_date)->subDay()->startOfDay()
            : now()->subDay()->startOfDay();

        $latest = $expiration->lessThan(now()->subDay()->startOfDay())
            ? $expiration
            : now()->subDay()->startOfDay();

        if ($latest->lt($effective)) {
            $effective = now()->subMonths(3)->startOfDay();
            $latest = now()->subDay()->startOfDay();
        }

        return Carbon::instance($faker->dateTimeBetween($effective, $latest));
    }

    protected function resolveStatus(): string
    {
        $statusPool = [
            'на розгляді' => 40,
            'схвалено' => 30,
            'виплачено' => 25,
            'відхилено' => 5,
        ];

        $roll = rand(1, array_sum($statusPool));
        $cursor = 0;

        foreach ($statusPool as $status => $weight) {
            $cursor += $weight;

            if ($roll <= $cursor) {
                return $status;
            }
        }

        return 'на розгляді';
    }

    protected function resolveAmountPaid(string $status, float $amountReserve): float
