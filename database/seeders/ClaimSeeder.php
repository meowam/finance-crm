    public function run(): void
    {
        $faker = Faker::create('uk_UA');

        $policies = Policy::query()
            ->with(['insuranceOffer.insuranceProduct', 'agent'])
            ->get();

        if ($policies->isEmpty()) {
            return;
        }

        $adminId = User::query()
            ->where('role', 'admin')
            ->value('id');

        $managerIds = User::query()
            ->where('role', 'manager')
            ->pluck('id')
            ->toArray();

        $fallbackUserId = $adminId ?: ($managerIds[0] ?? null);

        if (! $fallbackUserId) {
            return;
        }

        $claimablePolicies = $policies
            ->filter(function (Policy $policy) {
                if (! $policy->effective_date) {
                    return false;
                }

                return Carbon::parse($policy->effective_date)->startOfDay()->lte(now()->startOfDay());
            })
            ->values();

        if ($claimablePolicies->isEmpty()) {
            $claimablePolicies = $policies->values();
        }

        $targetCount = 100;
        $createdCount = 0;
        $claimsPerPolicy = max(1, (int) ceil($targetCount / max(1, $claimablePolicies->count())));

        foreach ($claimablePolicies as $policy) {
            $policyManagerId = $policy->agent_id ?: $fallbackUserId;

            for ($i = 0; $i < $claimsPerPolicy; $i++) {
                if ($createdCount >= $targetCount) {
                    break 2;
                }

                $productName = $policy->insuranceOffer?->insuranceProduct?->name ?? 'Невідомий продукт';
                $cause = $this->resolveCause($productName, $faker);
                $description = $this->resolveDescription($cause);

                $lossDate = $this->resolveLossDate($policy, $faker);
                $reportedAt = $lossDate->copy()->addDays(rand(0, 7));

                if ($reportedAt->greaterThan(now())) {
                    $reportedAt = now()->copy()->subHours(rand(1, 48));
                }

                $maxClaim = max(5000, (int) ($policy->coverage_amount ?: 50000));
                $amountClaimed = round($faker->numberBetween(3000, $maxClaim), 2);
                $amountReserve = round($amountClaimed * rand(50, 100) / 100, 2);

                $status = $this->resolveStatus();
                $amountPaid = $this->resolveAmountPaid($status, $amountReserve);

                $claim = Claim::query()->create([
                    'claim_number' => $this->makeClaimNumber(),
                    'policy_id' => $policy->id,
                    'reported_by_id' => $policyManagerId,
                    'status' => $status,
                    'reported_at' => $reportedAt,
                    'loss_occurred_at' => $lossDate,
                    'loss_location' => $faker->city() . ', вул. ' . $faker->streetName() . ', ' . $faker->buildingNumber(),
                    'cause' => $cause,
                    'amount_claimed' => $amountClaimed,
                    'amount_reserve' => $amountReserve,
                    'amount_paid' => $amountPaid,
                    'description' => $description,
                    'metadata' => null,
                ]);

                ClaimNote::query()->create([
                    'claim_id' => $claim->id,
                    'user_id' => $policyManagerId,
                    'visibility' => 'внутрішня',
                    'note' => $faker->randomElement($this->internalNotes()),
                ]);

                if ($faker->boolean(65)) {
                    ClaimNote::query()->create([
                        'claim_id' => $claim->id,
                        'user_id' => $policyManagerId,
                        'visibility' => 'зовнішня',
                        'note' => $faker->randomElement($this->externalNotes()),
                    ]);
                }

                $createdCount++;
            }
        }
    }

